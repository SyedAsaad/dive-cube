package com.demo.dive.cube.jrbeans;

public class BeachLogJrBean {
    private String logDate;
    private String personName;
    private String personCert;
    private String diveType;
    private String instructorName;
    private String totalPersons;
private String site;

    public String getPersonName() {
        return personName;
    }

    public void setPersonName(String personName) {
        this.personName = personName;
    }

    public String getPersonCert() {
        return personCert;
    }

    public void setPersonCert(String personCert) {
        this.personCert = personCert;
    }

    public String getDiveType() {
        return diveType;
    }

    public void setDiveType(String diveType) {
        this.diveType = diveType;
    }

    public String getInstructorName() {
        return instructorName;
    }

    public void setInstructorName(String instructorName) {
        this.instructorName = instructorName;
    }

    public String getTotalPersons() {
        return totalPersons;
    }

    public void setTotalPersons(String totalPersons) {
        this.totalPersons = totalPersons;
    }

    public String getLogDate() {
        return logDate;
    }

    public void setLogDate(String logDate) {
        this.logDate = logDate;
    }

    public String getSite() {
        return site;
    }

    public void setSite(String site) {
        this.site = site;
    }
}
