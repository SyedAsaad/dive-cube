package com.demo.dive.cube.jrbeans;

public class BoatLogJrBean {
    private String checkInSite;
    private String logDate;
    private String diveName;
    private String staffName;
    private String name;
    private String certificationLevel;
    private String boatCaptain;
    private String instructorName;

    public String getCheckInSite() {
        return checkInSite;
    }

    public void setCheckInSite(String checkInSite) {
        this.checkInSite = checkInSite;
    }

    public String getLogDate() {
        return logDate;
    }

    public void setLogDate(String logDate) {
        this.logDate = logDate;
    }

    public String getDiveName() {
        return diveName;
    }

    public void setDiveName(String diveName) {
        this.diveName = diveName;
    }

    public String getStaffName() {
        return staffName;
    }

    public void setStaffName(String staffName) {
        this.staffName = staffName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCertificationLevel() {
        return certificationLevel;
    }

    public void setCertificationLevel(String certificationLevel) {
        this.certificationLevel = certificationLevel;
    }

    public String getBoatCaptain() {
        return boatCaptain;
    }

    public void setBoatCaptain(String boatCaptain) {
        this.boatCaptain = boatCaptain;
    }

    public String getInstructorName() {
        return instructorName;
    }

    public void setInstructorName(String instructorName) {
        this.instructorName = instructorName;
    }
}
